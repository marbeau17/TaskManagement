-- =============================================================================
-- 064: 週キャパシティの標準値を 40h に統一
-- =============================================================================
-- 背景:
--   従来は users.weekly_capacity_hours の DEFAULT が 16.0 だった。
--   実運用では 30 名のメンバー全員で 40h/週 を標準稼働とするため、
--   既存メンバーの値を 40 に揃え、列のデフォルトも 40 に変更する。
-- =============================================================================

-- 1) 既存メンバー全員のキャパを 40h に統一（NULL も含めて確実に上書き）
UPDATE users
   SET weekly_capacity_hours = 40,
       updated_at = now()
 WHERE weekly_capacity_hours IS DISTINCT FROM 40;

-- 2) 列のデフォルト値を 16.0 → 40 に変更（今後 INSERT する新規ユーザー向け）
ALTER TABLE users
  ALTER COLUMN weekly_capacity_hours SET DEFAULT 40;
